package com.npu.hotelBooking.domain;

public class HotelInventory {
	
	private int economicRoom;
	private int deluxeRoom;
	private int executiveRoom;
	
	
	
	public int getEconomicRoom() {
		return economicRoom;
	}
	public void setEconomicRoom(int economicRoom) {
		this.economicRoom = economicRoom;
	}
	public int getDeluxeRoom() {
		return deluxeRoom;
	}
	public void setDeluxeRoom(int deluxeRoom) {
		this.deluxeRoom = deluxeRoom;
	}
	public int getExecutiveRoom() {
		return executiveRoom;
	}
	public void setExecutiveRoom(int executiveRoom) {
		this.executiveRoom = executiveRoom;
	}
}
